USE ROLE ROLE_FIN_DATA_ENGINEER;
USE DATABASE FIN_DATA_DEV;
CREATE SCHEMA IF NOT EXISTS SILVER;
USE SCHEMA SILVER;

-- Canonical Product Table
CREATE TABLE IF NOT EXISTS SILVER.PRODUCTS (
    PRODUCT_KEY        VARCHAR(50) PRIMARY KEY,
    PRODUCT_NAME       VARCHAR(255),
    CATEGORY           VARCHAR(100),
    UNIT_PRICE         NUMBER(15,2),
    SOURCE_SYSTEM      VARCHAR(50),
    TRACKINGHASH       VARCHAR(150),
    INGESTED_AT        TIMESTAMP_NTZ
);

-- Unified Canonical Customer Table
CREATE TABLE IF NOT EXISTS SILVER.CUSTOMERS (
    CUSTOMER_KEY       NUMBER IDENTITY(1,1),
    CUSTOMER_ID        VARCHAR(50),
    FIRST_NAME         VARCHAR(100),
    LAST_NAME          VARCHAR(100),
    EMAIL              VARCHAR(255),
    LOYALTY_TIER       VARCHAR(50),
    SIGNUP_SOURCE      VARCHAR(50),
    SEGMENT            VARCHAR(50),
    IS_ACTIVE          BOOLEAN,
    SOURCE_SYSTEM      VARCHAR(50),
    TRACKINGHASH       VARCHAR(150),
    INGESTED_AT        TIMESTAMP_NTZ,
    PRIMARY KEY (CUSTOMER_KEY)
);

-- Canonical Orders Table
CREATE TABLE IF NOT EXISTS SILVER.ORDERS (
    ORDER_KEY          NUMBER IDENTITY(1,1),
    ORDER_ID           VARCHAR(50),
    CUSTOMER_ID        VARCHAR(50),
    ORDER_DATE         DATE,
    ORDER_STATUS       VARCHAR(50),
    CHANNEL            VARCHAR(50),
    SOURCE_SYSTEM      VARCHAR(50),
    TRACKINGHASH       VARCHAR(150),
    INGESTED_AT        TIMESTAMP_NTZ,
    PRIMARY KEY (ORDER_KEY)
);

-- Canonical Payments Table
CREATE TABLE IF NOT EXISTS SILVER.PAYMENTS (
    PAYMENT_KEY        VARCHAR(50) PRIMARY KEY,
    ORDER_KEY          VARCHAR(50),
    PAYMENT_METHOD     VARCHAR(50),
    AMOUNT             NUMBER(15,2),
    PAYMENT_STATUS     VARCHAR(20),
    SOURCE_SYSTEM      VARCHAR(50),
    TRACKINGHASH       VARCHAR(150),
    INGESTED_AT        TIMESTAMP_NTZ
);